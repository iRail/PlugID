        <li><a href="http://twitter.com/jenssegers">Jens Segers</a></li>
        </ul>
        </p>
        <p>
        With PlugID, users can register on your service using their accounts on various social media sites. Currently supported are <a href="http://www.vikingspots.com">Vikingspots</a>, <a href="http://www.foursquare.com">Foursquare</a>, <a href="http://www.facebook.com">Facebook</a>, <a href="http://www.twitter.com">Twitter</a> and <a href="http://plus.google.com">Google+</a>.
        Once the user gives permission, your webservice can work with the connected account(s).
        </p>
        <p>
            PlugID is still in Alpha version. You can check out our progress (and contribute) on <a href="https://github.com/iRail/PlugID">GitHub</a>.</p>
